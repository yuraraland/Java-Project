
public class Calculatoer {
	
	public void powerOn() {
		//메서드
		System.out.println("전원을 켭니다");
	}
	
	public int plus (int x, int y) {
		//int 타입으로 반환한다
		int result = x + y;
		return result;
	}
	
	public double divide(int x, int y) {
		//double 타입으로 반환한다
		double result = (double)x / (double)y;
		return result;
	}
	
	void powerOff () {
		System.out.println("전원을 끕니다");
	}
	
	

}
